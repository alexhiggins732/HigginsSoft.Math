        public unsafe PrimeRecipricol(int prime)
        {
            Prime = prime;
            Recipricol = 1.0 / prime;
            RecipricolBits = BitConverter.DoubleToUInt64Bits(Recipricol);
            int[] words = new int[2];
            fixed (int* ptr = words)
            {
                *(ulong*)ptr = RecipricolBits;
            }

            // Access the values in the array
            int word1 = words[0];
            int word2 = words[1];
            BitString = $"{Convert.ToString(word2, 2).PadLeft(32, '0')}{Convert.ToString(word1, 2).PadLeft(32, '0')}";
        }
        public string FormattedBitString => $"{BitString.Substring(0, 1)} {BitString.Substring(1, 11)} {BitString.Substring(12, 52)}";
        public UnpackedDouble Unpack => new UnpackedDouble(this.RecipricolBits);

    }

